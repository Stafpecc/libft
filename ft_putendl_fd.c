#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		ft_putstr_fd(s[i], fd);
		i++;
	}
	ft_putstr_fd('\n', fd);
}
